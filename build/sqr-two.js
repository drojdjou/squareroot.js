/**
 *  Returns an orthographic projection matrix.
 */
SQR.ProjectionMatrix.prototype.orthographic = function(left, right, top, bottom, near, far) {

	var m = this.data;

	 /**
	 *  @property the near clipping
	 *  @readonly
	 */
	this.near = near;

	/**
	 *  @property the far clipping
	 *  @readonly
	 */
	this.far = far;
	
	var w = right - left;
	var h = top - bottom;
	var p = far - near;

	var x = ( right + left ) / w;
	var y = ( top + bottom ) / h;
	var z = ( far + near ) / p;

	m[0] = 2 / w;    m[4] = 0;        m[8] = 0;        m[12] = -x;
	m[1] = 0;        m[5] = 2 / h;    m[9] = 0;        m[13] = -y;
	m[2] = 0;        m[6] = 0;        m[10] = -2/p;    m[14] = -z;
	m[3] = 0;        m[7] = 0;        m[11] = 0;       m[15] = 1;
	return this;
}

/**
 *  Returns a perspective projection matrix.
 */
SQR.ProjectionMatrix.prototype.perspective = function(fov, aspect, near, far) {

	fov = fov || 60;
	near = near || 1;
	far = far || 1000;

	var m = this.data;
	var t = near * Math.tan(fov * Math.PI / 360);
	var n = far - near;

	m[0] = near / (t * aspect);
	m[4] =  0;
	m[8] =  0;
	m[12] = 0;

	m[1] = 0;
	m[5] = near / t;
	m[9] = 0;
	m[13] = 0;

	m[2] = 0;
	m[6] = 0;
	m[10] = -(far + near) / n;
	m[14] = -(2 * far * near) / n;

	m[3] = 0;
	m[7] = 0;
	m[11] = -1;
	m[15] = 0;

	/**
	 *  @property {Number} the field ov view
	 *	@default 60
	 *  @readonly
	 */
	this.fov = fov;

	/**
	 *  @property {Number=} the near clipping plane
	 *	@default 1
	 *  @readonly
	 */
	this.near = near;

	/**
	 *  @property {Number=} the far clipping plane
	 *	@default 1000
	 *  @readonly
	 */
	this.far = far;
	

	return this;
}

